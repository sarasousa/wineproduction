CREATE TABLE farm (
  fid INTEGER PRIMARY KEY,
  name TEXT
);

CREATE TABLE pandlet (
  pid INTEGER PRIMARY KEY,
  farm INTEGER REFERENCES farm(fid) NOT NULL,
  name TEXT NOT NULL,
  lat REAL NOT NULL,
  lng REAL NOT NULL,
  mac TEXT NOT NULL UNIQUE
);

CREATE TABLE person (
  uid INTEGER PRIMARY KEY,
  email TEXT NOT NULL UNIQUE,
  name TEXT NOT NULL,
 /* password TEXT NOT NULL,*/
  farm INTEGER REFERENCES farm(fid)
);

CREATE TABLE AuthorizedUser (
  email TEXT REFERENCES person (email) PRIMARY KEY,
  password TEXT NOT NULL
);

CREATE TABLE temperature (
  tid INTEGER PRIMARY KEY,
  farm INTEGER REFERENCES farm(fid) NOT NULL,
  pan INTEGER REFERENCES pandlet(pid) NOT NULL,
  value REAL NOT NULL,
  dates TEXT NOT NULL,
  times TEXT NOT NULL
);

CREATE TABLE temperaturesoil (
  tsid INTEGER PRIMARY KEY,
  farm INTEGER REFERENCES farm(fid) NOT NULL,
  pan INTEGER REFERENCES pandlet(pid) NOT NULL,
  value REAL NOT NULL,
  dates TEXT NOT NULL,
  times TEXT NOT NULL
);

CREATE TABLE moisture (
  mid INTEGER PRIMARY KEY,
  farm INTEGER REFERENCES farm(fid) NOT NULL,
  pan INTEGER REFERENCES pandlet(pid) NOT NULL,
  value REAL NOT NULL,
  dates TEXT NOT NULL,
  times TEXT NOT NULL
);

CREATE TABLE moisturesoil (
  msid INTEGER PRIMARY KEY,
  farm INTEGER REFERENCES farm(fid) NOT NULL,
  pan INTEGER REFERENCES pandlet(pid) NOT NULL,
  value REAL NOT NULL,
  dates TEXT NOT NULL,
  times TEXT NOT NULL
);


CREATE TABLE light (
  lid INTEGER PRIMARY KEY,
  farm INTEGER REFERENCES farm(fid) NOT NULL,
  pan INTEGER REFERENCES pandlet(pid) NOT NULL,
  value REAL NOT NULL,
  dates TEXT NOT NULL,
  times TEXT NOT NULL
);

